DROP TABLE CUSTOMER_2;
DROP TABLE INVOICE;


CREATE TABLE CUSTOMER (
CUST_NUM      INTEGER PRIMARY KEY, 
CUST_LNAME    VARCHAR(20), 
CUST_FNAME    VARCHAR(20), 
CUST_BALANCE  INTEGER);

